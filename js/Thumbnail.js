
class Thumbnail {
    /**
     * Create the thumbnail with the properties
     * @param {string} titre 
     * @param {string} author 
     * @param {string} date 
     * @param {string} profession 
     * @param {string} image 
     */
    constructor(title,author,date,profession,image) {
        this.title = title;
        this.author = author;
        this.date = date;
        this.profession = profession;
        this.image = image;
    }
}